package task_11_May;

public class OperatorEx {
	public static void main(String[] args) {
		
		int a = 5;
		int b = 2;
		
		a+=b; //a = a+b
		System.out.println(a);
		
		a-=b; // a = a-b;
		System.out.println(a);
		
		a*=b; // a = a*b;
		System.out.println(a);
		
		a/=b; //a = a/b;
		System.out.println(a);
		
		a%=b; //a = a%b;
		System.out.println(a);
	}
}
