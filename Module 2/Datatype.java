package automation1;

public class Datatype {

	public static void main(String[] args) {
		
		int a = 10 ; // integer value
		
		float b = 5.2F; //float value
		
		char c = 'k'; // character value
		
		double d = 25.7690; // double value
		
		System.out.println(a); // print integer value
		
		System.out.println(b);// print float value
		
		System.out.println(c); // print character value
		
		System.out.println(d); //print double value 
	}
}
